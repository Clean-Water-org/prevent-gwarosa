// 토큰 파서 — 라인 문자열을 세그먼트 배열로 분해
//   { plain }                              : 일반 텍스트
//   { typo:true, wrong, correct, cat }     : 오탈자(정답 — 클릭해야 함)
//   { trap:true, word, cat }               : 함정(정상 — 클릭하면 오답)
export function parseReportLine(str) {
  const out = [];
  const re = /«([^|»›]+)\|([^|»›]+)\|([^»›]+)[»›]|‹([^|»›]+)\|([^»›]+)[»›]/g;
  let last = 0;
  let m;
  while ((m = re.exec(str)) !== null) {
    if (m.index > last) out.push({ plain: str.slice(last, m.index) });
    if (m[1] != null) out.push({ typo: true, wrong: m[1], correct: m[2], cat: m[3] });
    else out.push({ trap: true, word: m[4], cat: m[5] });
    last = re.lastIndex;
  }
  if (last < str.length) out.push({ plain: str.slice(last) });
  return out;
}

